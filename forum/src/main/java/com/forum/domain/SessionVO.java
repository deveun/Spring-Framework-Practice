package com.forum.domain;

public class SessionVO {
	private String s_category;
	private String s_user_id;
	private String s_user_name;
	private String s_user_grade;
	private String s_type;
	private String s_search;
	
	public String getS_category() {
		return s_category;
	}
	public void setS_category(String s_category) {
		this.s_category = s_category;
	}
	public String getS_user_id() {
		return s_user_id;
	}
	public void setS_user_id(String s_user_id) {
		this.s_user_id = s_user_id;
	}
	public String getS_user_name() {
		return s_user_name;
	}
	public void setS_user_name(String s_user_name) {
		this.s_user_name = s_user_name;
	}
	public String getS_user_grade() {
		return s_user_grade;
	}
	public void setS_user_grade(String s_user_grade) {
		this.s_user_grade = s_user_grade;
	}
	public String getS_type() {
		return s_type;
	}
	public void setS_type(String s_type) {
		this.s_type = s_type;
	}
	public String getS_search() {
		return s_search;
	}
	public void setS_search(String s_search) {
		this.s_search = s_search;
	}
}
